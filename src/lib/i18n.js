export const copyKeys = Object.freeze(
  Object.keys(copy.ko).reduce((keys, key) => ({ ...keys, [key.replaceAll(".", "_")]: key }), {})
);

export function t(key, values = {}, locale = "ko") {
  const template = copy[locale]?.[key] ?? copy.ko[key] ?? key;
  return Object.entries(values).reduce(
    (text, [name, value]) => text.replaceAll(`{${name}}`, String(value)),
    template
  );
}
